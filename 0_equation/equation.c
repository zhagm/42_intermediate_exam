#include <stdio.h>

void	equation(int n)
{
	int a;
	int b;
	int c;

	a = 0;
	b = 0;
	c = 0;
	while (a != -1)
	{
		if ((((a * 10) + b) + ((c * 10) + a)) == n)
			printf("A = %d, B = %d, C = %d\n", a, b, c);
		if (c != 9)
			c++;
		else if (b != 9)
		{
			b++;
			c = 0;
		}
		else if (a != 9)
		{
			a++;
			b = 0;
			c = 0;
		}
		else
			a = -1;
	}
}
